def createVocabList(dataSet):
    vocabSet = set([])  #create empty set
    for document in dataSet:
        vocabSet = vocabSet | set(document) #union of the two sets
    return list(vocabSet)

def setOfWords2Vec(vocabList, inputSet):
    returnVec = [0]*len(vocabList)
    for word in inputSet:
        if word in vocabList:
            returnVec[vocabList.index(word)] = 1
        else: print ("the word: %s is not in my Vocabulary!" % word)
    return returnVec

#主要计算p(c0),p(c1),p(wi|c0),p(wi|c1)
#trainMatrix：文档矩阵
#trainCategory：每篇文档类别标签构成的向量
def trainNB0(trainMatrix,trainCategory):
    numTrainDocs = len(trainMatrix)     #文档词向量数量
    numWords = len(trainMatrix[0])      #每个词向量中的数量
    pAbusive = sum(trainCategory)/float(numTrainDocs)   #侮辱性文档数量占整个文档数量的概率，即是计算p(c0),p(c1)
    #为防止p(w0|1),p(w1|1)等一个值为0，导致整个乘积也为0
    #所以将所有词数初始化为1，分母初始化为2
    p0Num = ones(numWords); p1Num = ones(numWords)      #change to ones() 
    p0Denom = 2.0; p1Denom = 2.0                        #change to 2.0
    for i in range(numTrainDocs):
        if trainCategory[i] == 1:    #侮辱性的
            p1Num += trainMatrix[i]
            p1Denom += sum(trainMatrix[i])
        else:                                 #非侮辱性的
            p0Num += trainMatrix[i]
            p0Denom += sum(trainMatrix[i])
    #为防止下溢，采用对数的方式计算
    p1Vect = log(p1Num/p1Denom)          #change to log() #计算p(wi|c1)
    p0Vect = log(p0Num/p0Denom)          #change to log()  #计算p(wi|c0)
    return p0Vect,p1Vect,pAbusive

#vec2Classify：要分类的向量
#
def classifyNB(vec2Classify, p0Vec, p1Vec, pClass1):
    p1 = sum(vec2Classify * p1Vec) + log(pClass1)    #element-wise mult
    p0 = sum(vec2Classify * p0Vec) + log(1.0 - pClass1)
    if p1 > p0:
        return 1
    else: 
        return 0

#词袋向量转换模型
def bagOfWords2VecMN(vocabList, inputSet):
    returnVec = [0]*len(vocabList)
    for word in inputSet:
        if word in vocabList:
            returnVec[vocabList.index(word)] += 1
    return returnVec

